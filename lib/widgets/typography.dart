import 'package:flutter/material.dart';

// Standard Font Sizes for consistency across the app
class AppFontSizes {
  // Extra small - captions, hints
  static const double xs = 10.0;

  // Small - secondary text, labels
  static const double sm = 11.0;

  // Small medium - body text, descriptions
  static const double md = 12.0;

  // Medium - subtitles, emphasis
  static const double lg = 14.0;

  // Large - titles, headings
  static const double xl = 15.0;

  // Extra large - main titles
  static const double xxl = 18.0;

  // Heading - page headers
  static const double heading = 20.0;
}

// Standard Font Weights
class AppFontWeights {
  static const FontWeight regular = FontWeight.w400;
  static const FontWeight medium = FontWeight.w500;
  static const FontWeight semiBold = FontWeight.w600;
  static const FontWeight bold = FontWeight.bold;
}
